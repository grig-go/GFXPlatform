// utils/supabase/client.ts

import { createClient } from "@supabase/supabase-js";

const supabaseUrl = import.meta.env.VITE_NEXUS_SUPABASE_URL;
const supabaseAnonKey = import.meta.env.VITE_NEXUS_SUPABASE_ANON_KEY;

if (!supabaseUrl || !supabaseAnonKey) {
  throw new Error("Missing Supabase environment variables (VITE_NEXUS_SUPABASE_URL, VITE_NEXUS_SUPABASE_ANON_KEY). Check your .env file.");
}

export const supabase = createClient(supabaseUrl, supabaseAnonKey);
